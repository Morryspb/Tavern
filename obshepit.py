    # —Å–æ–∑–¥–∞—ë–º –æ–±—ä–µ–∫—Ç —Å —Å–æ–±—Å—Ç–≤–µ–Ω–Ω–æ–π –∏–∫–æ–Ω–∫–æ–π —Ä–∞–∑–º–µ—Ä–æ–º 30x30
    icon = CustomIcon(icon_url, icon_size=(30, 30))
    
    # —Å–æ–∑–¥–∞—ë–º –º–∞—Ä–∫–µ—Ä —Å –∏–∫–æ–Ω–∫–æ–π icon –∏ –¥–æ–±–∞–≤–ª—è–µ–º –µ–≥–æ –≤ –∫–ª–∞—Å—Ç–µ—Ä
